                    site, isto é, os preços dos produtos já aparecerão com valores menores. As lojas podem ter 2
                    comportamentos diferentes: (A) algumas mostrarão os descontos no valor dos produtos na listagem
                    (pode mostrar “de/por” ou mostrar somente o preço reduzido. É importante ficar atento para ter
                    certeza que o desconto foi realmente aplicado - ver Observação) ou (B) outras lojas só mostrarão o
                    desconto no Carrinho ou na tela de Pagamento (normalmente a informação do desconto estará
                    explícita).
